    private JournalistTableModel journalistTableModel;

    private JournalistOperations journalistSession;

    private Map<JTextComponent, JLabel> fields = new HashMap<>();

    private ModelMapper mapper;

    private Journalist selectedJournalist;

    private void initialize() {
        try {
            initValidation();
            hideErrors();
            initMapper();
            initSession();
            initTable();
        } catch (Exception ex) {
            Logger.getLogger(ManageJournalistsPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void initValidation() {
        fields.put(tfFirstName, lbFirstError);
        fields.put(tfLastName, lbLastError);
    }

    private void hideErrors() {
        fields.entrySet().forEach(e -> {
            e.getValue().setVisible(false);
        });
    }

    private void initMapper() {
        mapper = MapperFactory.InitializeMapper();
    }

    private void initSession() {
        journalistSession = HibernateSessionFactory.InitializeSession(Operations.JOURNALIST);
    }

    private void initTable() throws Exception {
        tbJournalist.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbJournalist.setAutoCreateRowSorter(true);
        tbJournalist.setRowHeight(25);

        journalistTableModel = new JournalistTableModel(journalistSession.ReadAllEntities().get());

        tbJournalist.setModel(journalistTableModel);
    }

    private void clearForm() {
        selectedJournalist = null;

        fields.entrySet().forEach(entry -> {
            entry.getKey().setText("");
        });
    }

    private void refreshTable() throws Exception {
        journalistTableModel = new JournalistTableModel(journalistSession.ReadAllEntities().get());
        tbJournalist.setModel(journalistTableModel);
    }

    private boolean formValid() {
        boolean ok = true;

        for (Map.Entry<JTextComponent, JLabel> entry : fields.entrySet()) {
            ok &= !entry.getKey().getText().trim().isEmpty();
            entry.getValue().setVisible(entry.getKey().getText().trim().isEmpty());
        }

        return ok;
    }

    private void showJournalist() throws Exception {
        int selectedRow = tbJournalist.getSelectedRow();
        int rowIndex = tbJournalist.convertRowIndexToModel(selectedRow);

        int id = (int) journalistTableModel.getValueAt(rowIndex, 0);
        selectedJournalist = journalistSession.ReadEntityById(id).get();
        fillForm(selectedJournalist);
    }

    private void fillForm(Journalist selectedCategory) {
        tfFirstName.setText(selectedCategory.getFirstName());
        tfLastName.setText(selectedCategory.getLastName());
    }
