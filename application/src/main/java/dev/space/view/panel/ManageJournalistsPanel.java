    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!formValid()) {
            return;
        }

        try {
            JournalistDTO journalist = new JournalistDTO(
                    tfFirstName.getText().trim(),
                    tfLastName.getText().trim()
            );

            journalistSession.InsertEntity(mapper.map(journalist, Journalist.class));

            refreshTable();
            clearForm();
        } catch (Exception ex) {
            MessageUtils.showErrorMessage("Error", ex.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedJournalist == null) {
            MessageUtils.showInformationMessage("Incorrect operations", "You must select journalist before deleting");
            return;
        }

        if (!formValid()) {
            return;
        }

        try {
            JournalistDTO journalist = new JournalistDTO(
                    selectedJournalist.getIdJournalist(),
                    tfFirstName.getText().trim(),
                    tfLastName.getText().trim()
            );

            Journalist map = mapper.map(journalist, Journalist.class);

            journalistSession.UpdateEntity(map);

            refreshTable();
            clearForm();
        } catch (Exception ex) {
            MessageUtils.showErrorMessage("Error", ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedJournalist == null) {
            MessageUtils.showInformationMessage("Incorrect operations", "You must select journalist before deleting");
            return;
        }

        if (MessageUtils.showConfirmDialog(
                "Delete article",
                "Do you really want to delete article?")) {
            try {
                journalistSession.DeleteEntity(selectedJournalist.getIdJournalist());

                journalistTableModel.setJournalists(journalistSession.ReadAllEntities().get());

                clearForm();
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Error", ex.getMessage());
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

